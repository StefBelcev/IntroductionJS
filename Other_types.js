// Null - indicates deliberate non-value (and is only accessible through the null keyword)
// Undefined - indicates an uninitialized
// Boolean type, with possible values true and false
console.log(Boolean(false));
console.log(Boolean(""));
console.log(Boolean(NaN));
console.log(Boolean(null));
console.log(Boolean(undefined));
console.log(Boolean(true));
console.log(Boolean(1));
console.log(Boolean("123"));